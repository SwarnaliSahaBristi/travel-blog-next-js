// GET all stories
export async function GET(req) {
  return new Response(JSON.stringify(stories), { status: 200 });
}

// POST a new story
export async function POST(req) {
  const body = await req.json();
  const newStory = {
    id: stories.length + 1,
    ...body,
    date: new Date().toISOString(),
  };
  stories.push(newStory);
  return new Response(JSON.stringify(newStory), { status: 201 });
}

// PUT (update) story by id
export async function PUT(req) {
  const { searchParams } = new URL(req.url);
  const id = parseInt(searchParams.get("id"));
  const body = await req.json();

  const index = stories.findIndex((s) => s.id === id);
  if (index === -1) return new Response("Story not found", { status: 404 });

  stories[index] = { ...stories[index], ...body };
  return new Response(JSON.stringify(stories[index]), { status: 200 });
}

// DELETE story by id
export async function DELETE(req) {
  const { searchParams } = new URL(req.url);
  const id = parseInt(searchParams.get("id"));

  const index = stories.findIndex((s) => s.id === id);
  if (index === -1) return new Response("Story not found", { status: 404 });

  const deleted = stories.splice(index, 1);
  return new Response(JSON.stringify(deleted[0]), { status: 200 });
}